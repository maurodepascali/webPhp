                    <h3>PASTA FROLA</h3>  
                    <img class="receta" style="float:right; margin:10px;" src="img/pastra%20frola.jpg" align="left">
                    <p><strong>Ingredientes</strong></p>
                        <ul>
                        <li>Yemas 1u</li>
                        <li>Azúcar 200grs</li>
                        <li>Esencia de vainilla 1cda</li>
                        <li>Huevo 1u</li>
                        <li>Harina leudante 400grs</li>
                        <li>Manteca 200grs</li>
                        <li>Dulce de membrillo 400grs</li>   
                        </ul>
                        <p><strong>Procedimientos</strong></p>
                        <p>Para la masa: batir la manteca pomada con el azúcar hasta formar una crema (unos 4 minutos con batidora eléctrica). Agregar la esencia de vainilla (que puede reemplazarse por ralladura de limón o naranja), el huevo y la yema. Seguir batiendo hasta obtener una crema homogénea. Incorporar la harina leudante con la sal. Para esto, dejamos de lado la batidora eléctrica y la cambiamos por una espátula de goma o cuchara de madera. La idea es integrar los secos sin amasar, no trabajar demasiado la masa. Con movimientos lentos, como aplastando, vamos uniendo los secos a esta preparación. Podemos usar las manos hacia el final, pero sin abusar. Si le damos demasiado trabajo, la masa quedaría chiclosa, y no tierna como buscamos. Una vez que la masa está bien integrada, se envuelve con papel film y se lleva a la heladera por media hora aproximadamente. Este paso es muy importante, porque si la masa no tiene suficiente frío y descanso, no vamos a poder estirarla correctamente. Una vez que la masa se enfrió, la sacamos de la heladera y la rompemos un poco con las manos para volver a unirla. La dividimos en dos bollos: 2/3 de masa para forrar la base y el tercio restante queda para hacer las tiras. Enharinamos un poco la mesada y estiramos la base hasta que quede de unos 3 mm de espesor aproximadamente. La enrollamos sobre el mismo palote y la colocamos en el molde de tarta enmantecado. Con las yemas de los dedos, nos aseguramos de que la masa llegue bien a todos los bordes del molde. Cortamos el excedente de masa alrededor de los bordes, y si es necesario, remendamos algún hueco que pueda haber quedado! Nadie se va a dar cuenta cuando la pastafrola esté lista. Para ablandar el dulce de membrillo, cortarlo en cubos y ponerlo en una cacerolita con un poquito de agua. Calentar lentamente e ir pisando con un tenedor. (Si en casa hay microondas, ponerlo en un plato apto para microondas con un poco de agua por 40 segundos). Colocar una capa del dulce de membrillo ablandado sobre la masa cruda. Hay dos formas de hacer las tiras de la pasta frola: una fácil y “rústica” y otra más compleja y prolijísima. Elige tu propia aventura: Forma fácil y rústica: Tomar pedacitos del tercio de masa restante y hacer choricitos finos y largos. Acomodar sobre la masa formando el clásico enrejado. Forma compleja y prolijísima: Estirar la masa con un palote formando un rectángulo. Colocarla sobre una placa metálica enharinada y llevar al freezer por media hora aproximadamente. Pasado ese tiempo, sacar la masa del freezer y cortar las tiras con un cuchillo afilado. (Acomodar sobre la masa formando el clásico enrejado). Pincelar las tiras con un huevo batido para que queden doraditas. Cocinar en horno medio por 40 minutos aproximadamente. Los bordes deben estar doraditos! Dejar enfriar para poder cortarla prolija.</p>
                   
