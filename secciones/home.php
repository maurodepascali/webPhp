					    <h3>TORTA OREO</h3>
                    <img class="receta" style="float:left; margin:30px;" src="img/oreo.jpg" align="right">
                    <p><strong>Ingredientes</strong></p>
                        <ul>
                        <li>1 paquete de galletitas oreo de 160gr</li>
                        <li>50gr de manteca</li>
                        <li>250gr de queso crema</li>
                        <li>150gr de azúcar</li>
                        <li>500ml de gelatina de 128g</li>
                        <li>300ml de leche entera</li>
                        <li>500ml de crema de leche</li>   
                        </ul>
                    <p><strong>Procedimientos</strong></p>
                    <p>Para elaborar la base de la tarta de Oreo, debemos separar las galletas Oreo y quitarles la crema que las une, reservaremos la crema. En este paso trituraremos todas las galletas tanto las de la base como las de la cobertura, dejaremos reservadas las galletas de la cobertura hasta su uso.
                    Trituraremos las galletas con la picadora eléctrica y las dejaremos en tipo polvo, mezclaremos con la mantequilla medio derretida, creando una masa que nos servirá de base para la tarta de oreo.
                    Colocaremos la masa sobre la base del molde ayudándonos de una cuchara o mismamente con la yema de los dedos, presionaremos bien la base y repartiremos toda la masa de forma homogénea por la base del molde, yo suelo aplastarla con la una tapa para dejar toda la base de galleta del mismo tamaño.
                    Para los que queráis des-moldar la tarta de la base es aconsejable poner un papel vegetal del mismo tamaño que la base, así  después podréis sacar la tarta sin ningún tipo de problema. Ya que si no le ponemos este papel será casi imposible y puede llegar a romperse toda la tarta. Meteremos el molde con la base de la galleta ya creada en el frigorífico para que se enfríe y nos quede más estable.
                    En un cazo ponemos a calentar la nata liquida, 150 ml de leche, el queso junto a el azúcar y la crema de las galletas que teníamos reservada. Dejar calentar a fuego bajo, mientras preparamos la cuajada.
                    En un vaso echaremos unos 150 ml de leche templada, diluir los sobres de cuajada y remover bien para que no queden grumos, yo los remuevo con un tenedor con bastante energía pero muchos de vosotros me comentáis que los hacéis con las varillas eléctricas y es una manera muy eficaz de que no queden esos grumos sin deshacer.
                    Añadir la cuajada a la mezcla que tenemos en el fuego. Remover de manera constante para que no se nos pegue la mezcla con unas varillas, debemos tener especial cuidado en que no se nos queme ya que tendría un gusto a quemado horrible y estropearía por completo la tarta. No pongáis el fuego muy alto, a fuego medio despacito sin dejar de remover y paciencia, así se disuelven bien todos los ingredientes de la mezcla.
                    Una vez que rompe a hervir, paso muy importante que hierva!!! No lo olvidéis!! Apartaremos del fuego y echaremos sobre el molde que teníamos en el frigorífico, verteremos la mezcla con especial cuidado y despacito.
                    Dejaremos templar la mezcla a temperatura ambiente y cuando notemos que ha perdido el calor, la meteremos a el frigorífico como mínimo unas tres horas para que cuaje bien.
                    Adornaremos nuestra tarta oreo con galletas picadas hechas polvo que teníamos reservadas, yo normalmente utilizo un colador de los que empleo para tamizar la harina, espolvorear la galleta sobre toda la superficie de la tarta y ya esta lista para degustar.
                    </p>
                    
